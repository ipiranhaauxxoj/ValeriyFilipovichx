package com.company;

public class Main {

    public static void main(String[] args) {

        //int to long
        int i_one = 2015;
        long l_one = (long) (i_one);
        System.out.println(l_one);

        //int to float
        int i_two = 2015;
        float f = (float) (i_two);
        System.out.println(f);

        //long to int
        long l_two = 214748364;
        int i_three = (int) l_two;
        System.out.println(i_three);

        //double to int
        double d = 3.14;
        int i_four = (int) d;
        System.out.println(i_four);

    }
}
